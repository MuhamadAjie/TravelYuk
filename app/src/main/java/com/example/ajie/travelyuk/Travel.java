package com.example.ajie.travelyuk;

class Travel {
    private String name;
    private String detail;
    private int photo;

    String getName() {
        return name;
    }

    void setName(String name) {
        this.name = name;
    }

    String getDetail() {
        return detail;
    }

    void setDetail(String detail) {
        this.detail = detail;
    }

    int getPhoto() {
        return photo;
    }

    void setPhoto(int photo) {
        this.photo = photo;
    }
}